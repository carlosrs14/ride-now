    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        Date date = dtcFechaNacimiento.getDate();
        int dia = 1;
        int mes = 1;
        int anio = 1;
        if (date != null) {
            Calendar calendar = Calendar.getInstance();
            calendar.setTime(date);
            dia = calendar.get(Calendar.DAY_OF_MONTH);
            mes = calendar.get(Calendar.MONTH) + 1; // porque van del 0 al 1
            anio = calendar.get(Calendar.YEAR);
        }
        
        String telefono = txtTelefono.getText().trim();
        String correo = txtEmail.getText();
        String password = String.copyValueOf(txtPassword.getPassword());
        Cliente cliente = ClienteController.crearCliente(nombre, apellido, dia, mes, anio, telefono, correo, password);
        
        if (ClientePersistencia.guardarCliente(cliente)) {
            JOptionPane.showMessageDialog(rootPane, "Guardado correctamente");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error al guardar este cliente");
        }
        
    }//GEN-LAST:event_btnRegistrarseActionPerformed

